#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	int				len;
	unsigned int	i;
	char			*str;

	if (!s)
		return (NULL);
	len = ft_strlen(s);
	i = 0;
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
